package environment;

public class GameMap {

	private float gravity;
	private float leftWind;
	private float rightWind;
	
	
	public float getGravity() {
		return gravity;
	}
	
	public void setGravity(float gravity) {
		this.gravity = gravity;
	}
	
	public float getLeftWind() {
		return leftWind;
	}
	
	public void setLeftWind(float leftWind) {
		this.leftWind = leftWind;
	}
	
	public float getRightWind() {
		return rightWind;
	}
	
	public void setRightWind(float rightWind) {
		this.rightWind = rightWind;
	}
	
	
	
}
